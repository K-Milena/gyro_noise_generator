volatile uint8_t mode = 0; // zmienna przechowująca aktualny tryb
#define SIN_FREQ       100 //100 - jeden z lepszych efektów
#define SAMPLING_RATE  48000
#define BUFFER_LENGTH  SAMPLING_RATE / SIN_FREQ
int16_t buffer_audio[2 * BUFFER_LENGTH];

void change_freq(int sound_freq)
{
	  for(int i = 0; i < BUFFER_LENGTH;i++)
	    {
	        buffer_audio[2 * i] = 10000 * sin(2 * 3.14 * sound_freq * i / SAMPLING_RATE);
	        buffer_audio[2 * i + 1] = 10000 * sin(2 * 3.14 * sound_freq * i / SAMPLING_RATE);
	    }
}

int16_t get_max_abs_value(int16_t x, int16_t y, int16_t z) {
    int16_t abs_x = abs(x);
    int16_t abs_y = abs(y);
    int16_t abs_z = abs(z);

    int16_t max_value = abs_x;
    if (abs_y > max_value) {
        max_value = abs_y;
    }
    if (abs_z > max_value) {
        max_value = abs_z;
    }
    return max_value;
}

int16_t get_min_abs_value(int16_t x, int16_t y, int16_t z) {
    int16_t abs_x = abs(x);
    int16_t abs_y = abs(y);
    int16_t abs_z = abs(z);

    int16_t min_value = abs_x;
    if (abs_y < min_value) {
        min_value = abs_y;
    }
    if (abs_z < min_value) {
        min_value = abs_z;
    }
    return min_value;
}

